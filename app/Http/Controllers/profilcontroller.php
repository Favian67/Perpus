            $user = Admin::with('user')->where('user_id', $id)->first();
        } else if (Auth::user()->role == 'petugas') {
            $user = Petugas::with('user')->where('user_id', $id)->first();
        } else {
            $user = Anggota::with('user')->where('user_id', $id)->first();
        }
        return view('profile', compact('user'));
    }

